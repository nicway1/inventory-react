// Get all country codes
export const getAllCountryCodes = (): string[] => Object.keys(COUNTRY_DATA);

// Get country by code
export const getCountryByCode = (code: string): CountryData | undefined => COUNTRY_DATA[code.toUpperCase()];

// Get country by slug
export const getCountryBySlug = (slug: string): CountryData | undefined => {
  return Object.values(COUNTRY_DATA).find(country => country.slug === slug.toLowerCase());
};

// Get countries by region
export const getCountriesByRegion = (region: string): CountryData[] => {
  return Object.values(COUNTRY_DATA).filter(country => country.region.toLowerCase() === region.toLowerCase());
};
